// 42. Una papelería vende libros a $10.000, cuadernos $ 7.550, y lapiceros a $5.550.
// Calcular el monto total de la venta, según la cantidad de artículos solicitados
// durante el día.

let libros = prompt("introduzca la cantidad de libros vendidos: ");
let cuadernos = prompt("introduzca la cantidad de cuadernos vendidos: ");
let lapiceros = prompt("introduzca la cantidad de lapiceros vendidos: ");

alert("la cantidad total de libros vendidos fue de: " + libros + " y el monto de los libros es de: " + (libros*10000));
alert("la cantidad total de cuadernos vendidos fue de: " + cuadernos + " y el monto de los libros es de: " + (cuadernos*7550));
alert("la cantidad total de lapiceros vendidos fue de: " + lapiceros + " y el monto de los libros es de: " + (lapiceros*5550));
